from random import randint

# تابعی برای اجرای بازی
def play_game():
    # انتخاب تصادفی بین 1 و 2
    a = randint(1, 2)

    # دریافت ورودی از کاربر
    b = input("راست یا چپ؟ (راست/چپ): ").strip().lower()  # ورودی را به حروف کوچک تبدیل می‌کنیم

    # بررسی نتیجه بازی
    if a == 1:
        print("...گل...")  # پیام برنده
        print("تو برنده شدی!")  # پیام برنده
        return True  # برنده شدن
    else:
        print("...پوچ...")  # پیام بازنده
        print("توباختی دوباره تلاش کن...")  # پیام بازنده
        return False  # باختن

# تابع اصلی
def main():
    # شمارش برنده‌ها و باخت‌ها
    wins = 0
    losses = 0

    # حلقه برای تکرار بازی
    while True:
        # اجرای بازی
        result = play_game()
        
        # به‌روزرسانی شمارش برنده‌ها و باخت‌ها
        if result:
            wins += 1
        else:
            losses += 1

        # نمایش وضعیت بازی
        print(f"شمار برنده‌ها: {wins}, شمار باخت‌ها: {losses}")

        # پرسش از کاربر برای ادامه یا پایان بازی
        play_again = input("آیا می‌خواهید دوباره بازی کنید؟ (بله/خیر): ").strip().lower()
        if play_again != 'بله':
            print("متشکرم که بازی کردید! خداحافظ!")
            break  # خروج از حلقه و پایان بازی

# اجرای تابع اصلی
if __name__ == "__main__":
    main()
