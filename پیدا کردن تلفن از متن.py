import re

def extract_phone_numbers(text):
    """
    این تابع شماره‌های تلفن را از متن ورودی استخراج می‌کند.
    
    پارامترها:
    text (str): متنی که شماره‌های تلفن در آن قرار دارد.

    بازگشت:
    list: لیستی از شماره‌های تلفن استخراج شده.
    """
    # الگوی جستجوی شماره تلفن
    # این الگو شماره‌های تلفن ایرانی با فرمت‌های مختلف را شناسایی می‌کند
    pattern = r'(\+98[- ]?\d{2}[- ]?\d{7}|\d{3}[- ]?\d{7}|\d{4}[- ]?\d{7}|\d{3}[- ]?\d{3}[- ]?\d{4}|\d{10})'
    
    # جستجو و استخراج شماره تلفن‌ها
    phone_numbers = re.findall(pattern, text)
    return phone_numbers

def main():
    """
    تابع اصلی برنامه که ورودی کاربر را دریافت کرده و شماره‌های تلفن را استخراج می‌کند.
    """
    # دریافت ورودی از کاربر
    text = input("اطلاعات و شماره خود را وارد کنید: ")
    
    # استخراج شماره تلفن‌ها
    phone_numbers = extract_phone_numbers(text)
    
    # نمایش شماره تلفن‌های استخراج شده
    print("شماره تلفن‌های استخراج شده:")
    if phone_numbers:
        for number in phone_numbers:
            print(number)
    else:
        print("شماره تلفنی یافت نشد.")

# اجرای برنامه
if __name__ == "__main__":
    main()
